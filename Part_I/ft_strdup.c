#include "../libft.h"

char	*ft_strdup(const char *s)
{
	size_t	len;
	char	*copy;

	len = ft_strlen(s) + 1;
	copy = malloc((unsigned int)len);
	if (copy != NULL)
		ft_strlcpy(copy, s, len);
	return (copy);
}
