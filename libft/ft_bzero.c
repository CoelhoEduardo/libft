#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	char	*str;
	int		i;

	str = (char *)s;
	i = 0;
	if (n == 0)
	{
		return ;
	}
	while (i < (int)n)
	{
		str[i] = 0;
		i++;
	}
}
